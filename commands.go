var commands map[string]cliCommand
var cache *pokecache.Cache
var caught map[string]pokemon

func init() {
	caught = make(map[string]pokemon)
	cache = pokecache.NewCache(10 * time.Second)
	commands = map[string]cliCommand{
		"exit": {
			name:        "exit",
			description: "Exit the Pokedex",
			callback:    commandExit,
		},
		"help": {
			name:        "help",
			description: "Displays a help message",
			callback:    commandHelp,
		},
		"map": {
			name:        "map",
			description: "Displays 20 areas. Each subsequent call shows the next 20 locations.",
			callback:    commandMap,
		},
		"mapb": {
			name:        "mapb",
			description: "Displays 20 areas. Each subsequent call shows the previous 20 locations.",
			callback:    commandMapb,
		},
		"explore": {
			name:        "explore",
			description: "Lists encounterable Pokemon in a location.",
			callback:    commandExplore,
		},
		"catch": {
			name:        "catch",
			description: "Attemps to catch a Pokemon.",
			callback:    commandCatch,
		},
		"inspect": {
			name:        "inspect",
			description: "Inspect a caught Pokemon.",
			callback:    commandInspect,
		},
		"pokedex": {
			name:        "pokedex",
			description: "Lists all caught pokemon.",
			callback:    commandPokedex,
		},
	}
}

func commandExit(cfg *config, parameters []string) error {
	fmt.Println("Closing the Pokedex... Goodbye!")
	os.Exit(0)
	return nil
}

func commandHelp(cfg *config, parameters []string) error {
	output := "Welcome to the Pokedex!\nUsage:\n\n"
	for _, command := range commands {
		output += fmt.Sprintf("%s: %s\n", command.name, command.description)
	}
	fmt.Print(output)
	return nil
}

func commandMap(cfg *config, parameters []string) error {
	if cfg.next == "" {
		fmt.Println("you're on the last page")
		return nil
	}
	cfg.previous, cfg.next = printLocationAreasOnPage(cfg.next)
	return nil
}

func commandMapb(cfg *config, parameters []string) error {
	if cfg.previous == "" {
		fmt.Println("you're on the first page")
		return nil
	}
	cfg.previous, cfg.next = printLocationAreasOnPage(cfg.previous)
	return nil
}

func commandExplore(cfg *config, parameters []string) error {
	printPokemonEncountersInLocation(parameters[0])
	return nil
}

func commandPokedex(cfg *config, parameters []string) error {
	fmt.Println("Your Pokedex:")
	for name := range caught {
		fmt.Printf("- %v\n", name)
	}
	return nil
}

func commandInspect(cfg *config, parameters []string) error {
	pokemon_info, isCaught := caught[parameters[0]]
	if !isCaught {
		fmt.Println("Not yet caught")
		return nil
	}
	fmt.Printf("Name: %s\n", pokemon_info.Name)
	fmt.Printf("Height: %v\n", pokemon_info.Height)
	fmt.Printf("Weight: %v\n", pokemon_info.Weight)
	fmt.Println("Stats:")
	for _, s := range pokemon_info.Stats {
		fmt.Printf("  -%s: %v\n", s.Stat.Name, s.BaseStat)
	}
	fmt.Println("Types:")
	for _, t := range pokemon_info.Types {
		fmt.Printf("  -%s\n", t.Type.Name)
	}
	return nil
}

func commandCatch(cfg *config, parameters []string) error {
	pokemon_response, err := cache.GetFrom(fmt.Sprintf("https://pokeapi.co/api/v2/pokemon/%s/", parameters[0]))
	if err != nil {
		fmt.Println("Error getting response from API or cache")
		return nil
	}

	result := pokemon{}
	err = json.Unmarshal(pokemon_response, &result)
	if err != nil {
		fmt.Println("Error unmarshaling response from API or cache")
		return nil
	}
	fmt.Printf("Throwing a Pokeball at %s...\n", parameters[0])
	if rand.Intn(700) >= result.BaseExperience {
		fmt.Printf("%s was caught!\n", parameters[0])
		caught[parameters[0]] = result
	} else {
		fmt.Printf("%s escaped!\n", parameters[0])
	}
	return nil
}

func printLocationAreasOnPage(url string) (string, string) {
	location_areas, err := cache.GetFrom(url)
	if err != nil {
		fmt.Println("Error getting response from API or cache")
		return "", ""
	}

	result := map_response{}
	err = json.Unmarshal(location_areas, &result)
	if err != nil {
		fmt.Println("Error unmarshaling response from API or cache")
		return "", ""
	}
	for _, location := range result.Results {
		fmt.Println(location.Name)
	}
	return result.Previous, result.Next
}

func printPokemonEncountersInLocation(location string) {
	location_area_response, err := cache.GetFrom(fmt.Sprintf("https://pokeapi.co/api/v2/location-area/%s/", location))
	if err != nil {
		fmt.Println("Error getting response from API or cache")
		return
	}

	result := location_response{}
	err = json.Unmarshal(location_area_response, &result)
	if err != nil {
		fmt.Println("Error unmarshaling response from API or cache")
		return
	}
	for _, pokemon := range result.PokemonEncounters {
		fmt.Println(pokemon.Pokemon.Name)
	}
}
